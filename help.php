
<p>Prozer ist eine Projektkommunikationsoftware für Agenturen. Optimiert für Agenturen bis ca. 50 Personen. </p>

<h3>Installation</h3>
<p>Es wird das AddOn <b>XForm</b> inklusive dem Plugin <b>Manager</b> benötigt. 
<br />Weiterhin wird das SystemAddOn <b>phpmailer</b> gebraucht.</p>

<h3>Einstieg</h3>
<p>Sobald die Installation und Aktivierung erfolgreich durchlaufen ist, kann man sich über das Frontend mit dem Login: <b>admin</b> und dem Passwort <b>admin</b> einloggen und hat einen kompletten PROZER Zugriff. Zunächst legt man die entsprechenden User an, dann Kunden und Projekte. Alles weitere wird in Kürze auf <a href="http://www.prozer.org">www.prozer.org</a> zu finden sein.</p>






